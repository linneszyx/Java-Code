class Arithmetic1 {
     public static void main (String[] args){
          int result = 3 + 2; // result is now 5
          System.out.println(result);
          result = result - 1; // result is now 4
          System.out.println(result);
          result = result * 2; // result is now 8
          System.out.println(result);
          result = result / 2; // result is now 4
          System.out.println(result);
          result = result + 8; // result is now 12
          result = result % 7; // result is now 5
          System.out.println(result);
     }
}
